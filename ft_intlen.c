#include "libft.h"

size_t		ft_intlen(int n)
{
	size_t	len;
	int		i;

	len = 0;
	i = 10;
	if (n == 0)
		return (1);
	while ((n / i) != 0)
	{
		i = i * 10;
		len++;
	}
	return (len);
}
