#include "libft.h"

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	i = (ft_strncmp(s1, s2, n) == 0 ? 1 : 0);
	return (i);
}
