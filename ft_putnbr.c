#include "libft.h"

void	ft_putnbr(int n)
{
	char	nbr;

	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		nbr = n % 10;
		nbr += '0';
		ft_putnbr(n / 10);
		ft_putchar(nbr);
	}
	else
		ft_putchar(n + '0');
}
